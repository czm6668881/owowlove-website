# Test upload-image endpoint
Write-Host "`n🔄 Testing /api/admin/upload-image endpoint..." -ForegroundColor Yellow
try {
    $response1 = curl -X POST -F "image=@$testImagePath" http://localhost:3001/api/admin/upload-image -v 2>&1
    Write-Host "📋 Response from upload-image:" -ForegroundColor Cyan
    Write-Host $response1 -ForegroundColor White
} catch {
    Write-Host "❌ Error testing upload-image: $($_.Exception.Message)" -ForegroundColor Red
}

# Test upload endpoint
Write-Host "`n🔄 Testing /api/admin/upload endpoint..." -ForegroundColor Yellow
try {
    $response2 = curl -X POST -F "file=@$testImagePath" http://localhost:3001/api/admin/upload -v 2>&1
    Write-Host "📋 Response from upload:" -ForegroundColor Cyan
    Write-Host $response2 -ForegroundColor White
} catch {
    Write-Host "❌ Error testing upload: $($_.Exception.Message)" -ForegroundColor Red
}

# Clean up
if (Test-Path $testImagePath) {
    Remove-Item $testImagePath
    Write-Host "`n🧹 Cleaned up test file" -ForegroundColor Green
}

Write-Host "`n✅ Test completed" -ForegroundColor Green
